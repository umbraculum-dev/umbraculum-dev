import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";

const pkgRoot = path.resolve(import.meta.dirname, "..");
const assetsDir = path.join(pkgRoot, "assets");
const outFile = path.join(pkgRoot, "src", "manifest.generated.ts");
const outJsonFile = path.join(pkgRoot, "src", "manifest.generated.json");

function toPosix(p) {
  return p.split(path.sep).join("/");
}

function sha256Hex(buf) {
  return crypto.createHash("sha256").update(buf).digest("hex");
}

function shortHash(hex) {
  return hex.slice(0, 12);
}

/** @param {string} dir */
function listFiles(dir) {
  /** @type {string[]} */
  const out = [];
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) out.push(...listFiles(p));
    else if (ent.isFile()) out.push(p);
  }
  return out;
}

if (!fs.existsSync(assetsDir)) {
  throw new Error(`assets dir not found: ${assetsDir}`);
}

const files = listFiles(assetsDir).sort();

/** @type {Record<string, { publicPath: string; sha256: string; bytes: number }>} */
const manifest = {};

for (const abs of files) {
  const rel = toPosix(path.relative(assetsDir, abs));
  const buf = fs.readFileSync(abs);
  const sha = sha256Hex(buf);
  const bytes = buf.byteLength;

  const ext = path.extname(rel);
  const withoutExt = ext ? rel.slice(0, -ext.length) : rel;
  const hashedName = `${withoutExt}.${shortHash(sha)}${ext}`;

  manifest[rel] = {
    publicPath: `/media/${hashedName}`,
    sha256: sha,
    bytes,
  };
}

const lines = [];
lines.push("// This file is generated by scripts/generate-manifest.mjs");
lines.push("// Do not edit manually.");
lines.push("");
lines.push("export const mediaManifest = {");

for (const key of Object.keys(manifest)) {
  const entry = manifest[key];
  lines.push(`  ${JSON.stringify(key)}: { publicPath: ${JSON.stringify(entry.publicPath)}, sha256: ${JSON.stringify(entry.sha256)}, bytes: ${entry.bytes} },`);
}

lines.push("} as const;");
lines.push("");
lines.push("export type MediaAssetKey = keyof typeof mediaManifest;");
lines.push("");

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, lines.join("\n"));

fs.writeFileSync(outJsonFile, JSON.stringify(manifest, null, 2) + "\n");

